package yhtommik.Todo;

public class TodoTheme {
    private String color;
    private String image;

    public TodoTheme(String color, String image){
        this.color = color;
        this.image = image;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
